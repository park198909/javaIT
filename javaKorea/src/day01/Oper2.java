package day01;

public class Oper2 {
	public static void main(String[] args) {
		String 글자1 = "Hello";
		String 글자2 = "Java";
		String 글자 = 글자1+글자2;
		
		System.out.println(글자);
		
		System.out.println("일\t이\n"); 
		//  \t : 칸 띄우기
		System.out.println("안녕\n하세요\n"); 
		//  \n : 한줄 띄우기
		System.out.println("아무개는 말했다. \"빈기워\""); 
		//  \" : 큰 따움표 사용하기
		
		
	}
}
